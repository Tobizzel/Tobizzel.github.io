var cacheVersion = 2;
var currentCache = {
  offline: 'offline-cache' + cacheVersion
};
const offlineUrl = 'offline.html';

this.addEventListener('install', event => {
  event.waitUntil(
    caches.open('v3').then(function(cache) {
      return cache.addAll([
          'index.html',
      ]);
    })
  );
});

